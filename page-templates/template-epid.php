                我來自於中國醫藥大學公共衛生學系，大學畢業後因為自己對著醫學與公共衛生的發展抱持著好奇的心態，所以於大學畢業前夕便決定報考研究所，很幸運地來到陽明公衛所。到陽明報到的第一天，令我印象最深刻的是每位學生的背景，這項特色將重新定義我對公共衛生的認識，這也是陽明公衛所有別於一般研究所的性質。大學以來我一直環繞在公共衛生的領域，但不諱言的和大家分享，直到大學畢業前才對於公共衛生有基礎上的認識，認識甚麼是公共衛生、流行病學、生物統計、衛生政策等，因為公共衛生要了解的知識太多除此之外還需要有實務上的經歷才能慢慢理解公共衛生的邏輯概念。來到陽明公衛所，因為這裡的學生來自社會上四面八方的人才，所以在上課的時候常常能聽到各種不同的角度來看待同一件事情。縱然用不同的角度來討論一件事情是學習上的好處，但要怎麼融合成各位學生能理解並且認同的知識，這就需要仰賴陽明公衛所的每一位老師。
                陽明公衛所的第二項特色即是師資。公共衛生最初的目的便是將專業的知識傳達給公眾理解，但是從專業到非專業的過程中需要一道橋梁，來連接中間的鴻溝，但是這段鴻溝的管道會有很多種形式出現，所以在國外公共衛生的教學網羅了各專業領域的師資，透過他們的專業，重新定義公共衛生的概念，再次傳達給學生了解。陽明公衛所同樣也看到相似的情境，因此在師資的選擇上不單單只針對醫學、公共衛生，更有法律、統計、運動等等專業的師資來傳達公共衛生。同時在課程的開設上，也不只針對公共衛生的知識學習，更針對不同的角度來審視公共衛生在不同領域中的實務應用。這裡的老師同樣地也受到專業且多元的訓練，所以在各種課堂的討論中，除了學生們的熱烈回應外，老師們也同樣能提出相對應的回答來統合各界的認知重新傳達公共衛生。
                我在陽明公衛所碩士生活的兩年，回首過去這段日子，論文撰寫的過程真的很辛苦但努力過後的興奮絕對遠大於之前辛苦的過程，雖然過程艱辛但陽明公衛所就像個大家庭，上至所長、老師下至學、弟妹，大家和樂融融照應彼此。過年過節時分，這裡的研究生依然持續作業，但總不忘過節的氣氛，老師們也常常出人意表的傳達過節氛圍，這段過程將會是我整個學習旅程中最棒的回憶，若問我能有機會重新選擇繼續進修的地方，我相信我依然還是會選擇陽明公共衛生研究所！
