        <article id="events_main_article">
            這不只是一本突破性的健康與科學書籍，更是一個感動人心的故事，因為眾多高齡修女們的慷慨奉獻，我們才能活得更久、活得更好。老年如何過得優雅？經過一生的學習和打拚，到了歲月將盡之時，究竟能以豐富的大腦功能安享天年？還是記憶失喪、情緒失控，成為別人的負擔？
            <br><br>
            為了探索人類有史以來最剝奪人性尊嚴的阿茲海默症，一群修女甘願每年接受嚴格的測驗和訪談，死後捐出大腦，揭開了成功老化和長壽的秘訣：
            <br><br>
            　　◆為什麼童年時建立語言能力能預防阿茲海默症？<br>
            　　◆哪些常見食物有助於長壽與大腦的功能？<br>
            　　◆為何預防中風和憂鬱症是避免阿茲海默症的關鍵？<br>
            　　◆遺傳扮演了什麼角色？為什麼規律運動永不嫌遲？<br>
            　　◆態度、信仰及社群如何能延年益壽？<br><br>
            　　一九八六年，斯諾登博士和一群住在明尼蘇達州的修女們開始進行修女研究（Nun Study）。研究問題為：人在早年、中年及老年階段，增加阿茲海默症（老年癡呆症）及其他大腦疾病，例如，中風的變因為何？參與研究的對象為678名74~106歲的修女，平均年紀為83歲，其中，超過85%的修女是教師。這群來自聖母院學校修女會的研究對象，每年都需接受身體檢查。最後，在臨終時，同意把大腦捐出來提供腦神經學家研究。這678位偉大的修女，是全世界最大且同質性最高的大腦捐贈團體。要人家捐大腦？很難想像的一件事！一九九○年的某天，斯諾登博士面對著這群修女要說出這個請求，一大群人坐著，氣氛可想而知。靜默中間，一位修女說：「嗯，他當然可以把我的大腦拿去，我人都已經死了，還留著大腦幹什麼？」
            <br><br>
            　　修女研究於一九九一年收到第一個捐贈者的大腦，是一位89歲且神智清楚的Nicolette修女。這群研究對象中，有的人患有阿茲海默症，有的在極高歲數時頭腦還是相當清楚。研究發現，基因是阿茲海默症的一個重要因素外，生活方式（尤其兒時）也有很大關聯性。
            <br><br>
            　　1. 兒時學習越多的技能（大腦接受越多的刺激），受阿茲海默症侵害或罹患認知癡呆的機率越低。
            　　2. 腦神經科學中，在幼兒發展時常說的一句話：不用即棄！在這個研究中似乎得到驗證。
            <br><br>
            　　修女研究要問的是，為什麼有些修女能夠如此優雅地老去，持續教書與服事，一直到八十歲、九十歲，甚至超過一百歲，都還維持著完好的心智功能？為什麼其他一直都過著如此相似生活的修女，卻似乎會失去自我，忘記自己最親近的朋友、親人，到最後甚至跟身邊的世界完全失去聯繫？
            <br><br>
            　　聖母學校修女會的修女們讓我明白，老年不是我們應該害怕或侮蔑的事情。它可以是一段充滿可能與新生的時期，讓我們可以用洞悉一切的眼光觀察並接受人生教我們的功課，並且在可能時，將這些教訓傳給以後的世代。在現代世界裡，要長時間追蹤像聖母學校修女會這樣一個定義清晰又如此同質的人口群體，機會已經越來越稀少了。這是一部很可能永遠無法再重拍的電影……
            <br><br>
            關於這項千載難逢的研究：
            「我們只徵召在一九九一年時已經年滿75歲的修女，總結起來，總共1027位符合資格的修女中，有678位加入了大腦捐獻計畫。這展現了驚人的66%的參與率，截至二○○一年初，我們已經解剖了修女研究中九位百歲人瑞的大腦……」
